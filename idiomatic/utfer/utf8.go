func (t utf8) ToUtf7(fillChar UtfChar, chars ...UtfChar) []UtfChar {
	var utf7 []UtfChar

	for _, char := range chars {
		if found, ok := t.utf8Toutf7[char]; ok {
			utf7 = append(utf7, found)
		} else {
			utf7 = append(utf7, fillChar)
		}
	}

	return utf7
}

